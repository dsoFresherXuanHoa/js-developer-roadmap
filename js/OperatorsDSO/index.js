let x = 20;
let y = 25;

console.log("Result of x + y: " + (x + y));
console.log("Result of x - y: " + (x - y));
console.log("Result of x * y: " + x * y);
console.log("Result of x ** y: " + x ** y);
console.log("Result of x / y: " + x / y);
console.log("Result of x % y: " + (x % y));
console.log("Previous value of x: " + --x);
console.log("Next value of y: " + ++y);
